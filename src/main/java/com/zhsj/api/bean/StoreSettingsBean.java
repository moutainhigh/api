package com.zhsj.api.bean;

public class StoreSettingsBean {

	private int id;
	private String storeNo;
	private int cloudPrint;
	private int eleInvoice;
	private int isTest;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStoreNo() {
		return storeNo;
	}
	public void setStoreNo(String storeNo) {
		this.storeNo = storeNo;
	}
	public int getCloudPrint() {
		return cloudPrint;
	}
	public void setCloudPrint(int cloudPrint) {
		this.cloudPrint = cloudPrint;
	}
	public int getEleInvoice() {
		return eleInvoice;
	}
	public void setEleInvoice(int eleInvoice) {
		this.eleInvoice = eleInvoice;
	}
	public int getIsTest() {
		return isTest;
	}
	public void setIsTest(int isTest) {
		this.isTest = isTest;
	}
	
	
	
}
