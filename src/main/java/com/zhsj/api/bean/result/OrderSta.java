package com.zhsj.api.bean.result;

public class OrderSta {

	private int count;
	private double pm;
	private double am;
	private double orgDiscount;
	private double storeDiscount;
	
	private double average;
	
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public double getPm() {
		return pm;
	}
	public void setPm(double pm) {
		this.pm = pm;
	}
	public double getAm() {
		return am;
	}
	public void setAm(double am) {
		this.am = am;
	}
	public double getOrgDiscount() {
		return orgDiscount;
	}
	public void setOrgDiscount(double orgDiscount) {
		this.orgDiscount = orgDiscount;
	}
	public double getStoreDiscount() {
		return storeDiscount;
	}
	public void setStoreDiscount(double storeDiscount) {
		this.storeDiscount = storeDiscount;
	}
	public double getAverage() {
		return average;
	}
	public void setAverage(double average) {
		this.average = average;
	}
	
	
	
	
}
